package ch04.lecture.p04for;

public class C01For {
	public static void main(String[] args) {
		// 반복문 while
		/*
		이전코드
		int = 0
		whie(i<3){ 반복할코드 i++}
		다음코드
		*/
		
		System.out.println("이전 코드...");
		
		for (int i = 0; i < 3; i++) {
			System.out.println("반복할 코드1");
			System.out.println("반복할 코드2");
		}
		
		System.out.println("실행 이어감..");
		

	}
}
